class CNN(nn.Module):
    def __init__(self):
        super(CNN, self).__init__()
        self.conv1 = nn.Conv2d(in_channels=60, out_channels=64, kernel_size=3)
        self.pool1 = nn.MaxPool2d(kernel_size=2)
        self.conv2 = nn.Conv2d(in_channels=64, out_channels=128, kernel_size=3)
        self.pool2 = nn.MaxPool2d(kernel_size=2)
        self.fc1 = nn.Linear(in_features=128*13*9, out_features=1024)
        self.fc2 = nn.Linear(in_features=1024, out_features=512)

    def forward(self, x):
        x = self.conv1(x)
        x = F.relu(x)
        x = self.pool1(x)
        x = self.conv2(x)
        x = F.relu(x)
        x = self.pool2(x)
        # print('CNN:', x.shape)
        x = x.view(-1, 128*13*9)
        x = F.relu(self.fc1(x))
        x = self.fc2(x)
        return x

class RNN(nn.Module):
    def __init__(self):
        super(RNN, self).__init__()
        self.lstm = nn.LSTM(input_size=60*42, hidden_size=64, num_layers=4, batch_first=True)
        self.fc = nn.Linear(in_features=64, out_features=512)

    def forward(self, x):
        x = torch.flatten(x,2)
        out, (hidden, cell) = self.lstm(x)
        # print(out.shape, hidden.shape, cell.shape)
        out = self.fc(out[:, -1, :])
        return out

class MultiHeadAttention(nn.Module):
    def __init__(self):
        super(MultiHeadAttention, self).__init__()
        self.query = nn.Linear(512, 64)
        self.key = nn.Linear(512, 64)
        self.value = nn.Linear(128, 64)
        self.multihead = nn.MultiheadAttention(embed_dim=64, num_heads=8)
        self.fc = nn.Linear(64, 512)

    def forward(self, cnn_out, rnn_out):
        # print(cnn_out.shape, rnn_out.shape)
        cnn_out = self.query(cnn_out)
        rnn_out = self.key(rnn_out)
        query = torch.unsqueeze(cnn_out, dim=2).permute(2, 0, 1)
        key = torch.unsqueeze(rnn_out, dim=2).permute(2, 0, 1)
        value = torch.cat((cnn_out, rnn_out), dim=1)
        value = torch.unsqueeze(self.value(value), dim = 2).permute(2, 0, 1)
        attn_output, attn_weights = self.multihead(query, key, value)
        attn_output = attn_output.permute(1, 0, 2)
        out = self.fc(attn_output[:, -1, :])
        return out

class Ensemble(nn.Module):
    def __init__(self):
        super(Ensemble, self).__init__()
        self.cnn = CNN()
        self.rnn = RNN()
        self.attention = MultiHeadAttention()

    def forward(self, x):
        cnn_out = self.cnn(x)
        # print('1', cnn_out.shape)
        # print(x.shape)
        rnn_out = self.rnn(x)
        # print('2', rnn_out.shape)
        out = self.attention(cnn_out, rnn_out)
        # print('Ensemble',out.shape)
        return out
